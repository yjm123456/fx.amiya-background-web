<script>
import * as api from "@/api/orderManage";
import { download } from "@/utils/util";
export default {
  props: {
    orderListReportModal: {
      type: Boolean,
    },
  },
  data() {
    return {
      //合计应收款
      receivablesNum:0,
      //合计实付款
      actualPaymentNum: 0,
      //合计支付积分
      payPoints: 0,
      //合计总条数
      pageCount: 0,
      // 合计总数量
      purchaseQuantity:0,
      orderListReportModals: false,
      query: {
        orderType: [
          {
            statusText: "等待买家付款",
            status: "WAIT_BUYER_PAY",
          },
          {
