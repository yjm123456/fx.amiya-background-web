          },
          {
            statusText: "等待买家确认收货",
            status: "WAIT_BUYER_CONFIRM_GOODS",
          },
          {
            statusText: "买家已签收,货到付款专用",
            status: "TRADE_BUYER_SIGNED",
          },
          {
            statusText: "交易成功",
            status: "TRADE_FINISHED",
          },
          {
            statusText: "付款以后用户退款成功，交易自动关闭",
            status: "TRADE_CLOSED",
          },
          {
            statusText: "付款以前，卖家或买家主动关闭交易",
            status: "TRADE_CLOSED_BY_TAOBAO",
          },
        ],
        statusCode: "",
        startDate: this.$moment()
          .startOf("month")
          .format("YYYY-MM-DD"),
        endDate: this.$moment(new Date()).format("YYYY-MM-DD"),
        columns: [
          {
            title: "订单号",
            key: "id",
            minWidth: 200,
            align: "center",
          },
          {
            title: "下单平台",
            key: "appTypeText",
            minWidth: 100,
            align: "center",
          },
          {
            title: "下单日期",
            minWidth: 180,
            align: "center",
            key: "createDate",
            render: (h, params) => {
              return h(
                "div",
                this.$moment(params.row.createDate).format(
                  "YYYY-MM-DD HH:mm:ss"
                )
              );
            },
          },
          {
            title: "核销时间",
            align: "center",
            minWidth: 180,
            key: "writeOffDate",
            render: (h, params) => {
              return h(
                "div",
                params.row.writeOffDate ? this.$moment(params.row.writeOffDate).format(
                  "YYYY-MM-DD HH:mm:ss"
                ) : ''
              );
            },
          },
          {
            title: "订单性质",
            key: "orderNatureText",
            minWidth: 100,
