          },
        ],
        data: [],
      },
    };
  },
  methods: {
    // 获取下单平台报表
    getsendOrderReport() {
      const { startDate, endDate, statusCode } = this.query;
      const data = {
        startDate: this.$moment(startDate).format("YYYY-MM-DD"),
        endDate: endDate ? this.$moment(endDate).format("YYYY-MM-DD") : "",
        statusCode: statusCode,
      };
      if (!startDate || !endDate) {
        this.$Message.error("请选择日期");
        return;
      }
      api.OrderReport(data).then((res) => {
        if (res.code === 0) {
          const { customerOrderReceivableReport } = res.data;
          this.query.data = customerOrderReceivableReport;
          this.pageCount = this.query.data.length;
          let actualPaymentNum = 0;
          let payPoints = 0;
          let purchaseQuantity = 0;
          let receivablesNum = 0
          this.query.data.map((item, index) => {
            actualPaymentNum += Number(item.actualPayment);
            payPoints += Number(item.integrationQuantity);
            purchaseQuantity += Number(item.quantity);
            receivablesNum += Number(item.accountReceivable);
          });
          this.actualPaymentNum = Math.floor(actualPaymentNum * 100) / 100;
          this.payPoints = Math.floor(payPoints * 100) / 100;
          this.purchaseQuantity = Math.floor(purchaseQuantity * 100) / 100;
          this.receivablesNum = Math.floor(receivablesNum * 100) / 100;

        }
      });
    },
    // 导出
    exportsendOrder() {
      const { startDate, endDate, statusCode } = this.query;
      const data = {
        startDate: this.$moment(startDate).format("YYYY-MM-DD"),
        endDate: endDate ? this.$moment(endDate).format("YYYY-MM-DD") : "",
        statusCode: statusCode,
      };
      if (!startDate || !endDate) {
        this.$Message.error("请选择日期");
        return;
      }
      if (this.query.data.length == 0) {
        this.$Message.error("没有数据时不支持导出");
        return;
      }
      api.OrderReportExport(data).then((res) => {
        let name =
          this.$moment(new Date(startDate)).format("YYYY-MM-DD") +
          "-" +
          this.$moment(new Date(endDate)).format("YYYY-MM-DD") +
          "下单平台订单报表";
        download(res, name);
      });
    },
    handleModalVisibleChange(value) {
      if (!value) {
        this.isEdit = false;
        // this.$refs["form"].resetFields();
        this.$emit("update:orderListReportModal", false);
      }
    },
    handleModalVisibleChange(value) {
      if (!value) {
        this.isEdit = false;
        // this.$refs["form"].resetFields();
        this.$emit("update:orderListReportModal", false);
        this.query.data = [];
        this.query.startDate = this.$moment()
          .startOf("month")
          .format("YYYY-MM-DD");
        this.query.endDate = this.$moment(new Date()).format("YYYY-MM-DD");
        this.actualPaymentNum = 0;
        this.payPoints = 0;
        this.pageCount = 0;
        this.purchaseQuantity = 0;
        this.receivablesNum = 0;
        this.query.statusCode = "";
      }
    },
    // 取消
    cancelSubmits(name) {
      this.isEdit = false;
      this.control = false;
      this.$emit("update:orderListReportModal", false);
      this.query.data = [];
      this.query.startDate = this.$moment()
        .startOf("month")
        .format("YYYY-MM-DD");
      this.query.endDate = this.$moment(new Date()).format("YYYY-MM-DD");
      this.actualPaymentNum = 0;
      this.payPoints = 0;
      this.pageCount = 0;
      this.purchaseQuantity = 0;
      this.receivablesNum = 0;
      this.query.statusCode = "";
    },
  },
  created() {
    // this.getsendOrderReport()
  },
  watch: {
    orderListReportModal(value) {
      this.orderListReportModals = value;
    },
  },
};
