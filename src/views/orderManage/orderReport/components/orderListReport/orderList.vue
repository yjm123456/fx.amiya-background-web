        >查询</Button
      >
      <Button type="primary" @click="exportsendOrder" v-has="{ role: ['fx.amiya.permission.EXPORT'] }">导出</Button>
      <Card class="container">
        <div>
          <Table
            border
            :columns="query.columns"
            :data="query.data"
            height="700"
          ></Table>
        </div>
      </Card>
      <div slot="footer" class=" foot">
        <div style="display:flex">
          <div class="num">
            合计支付积分：
            <div style="color:red">{{ payPoints }}</div>
          </div>
          <div class="num">
            合计实付款：
            <div style="color:red">{{ actualPaymentNum }}</div>
          </div>
          <div class="num">
            合计应收款：
            <div style="color:red">{{ receivablesNum }}</div>
          </div>
          <div class="num">
            合计购买数量：
            <div style="color:red">{{ purchaseQuantity }}</div>
          </div>
          <div class="num">
            总条数：
            <div style="color:red">{{ pageCount }}</div>
          </div>
        </div>
        <Button @click="cancelSubmits()">关闭页面</Button>
        <!-- <Button type="primary" @click="handleSubmit()">确定</Button> -->
      </div>
    </Modal>
  </div>
